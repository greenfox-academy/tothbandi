
'use strict';

var massInKg = 81.2;
var heightInM = 1.78;

// Print the Body mass index (BMI) based on these values

var bmi = 0;

bmi = massInKg / (heightInM * heightInM);

console.log('Mass is: ' + massInKg + 'kg.');
console.log('Height id: ' + heightInM + 'm.');
console.log('BMI is: ' + bmi + '.');